(() => {

    function changeBackground(color) {
        document.body.style.background = color;
    }

    red.addEventListener("mouseover",function() { changeBackground('red') }); /* FUUUUUUUUUUNNNNNNNNNN */
    green.addEventListener("click",function() { changeBackground('green') });
    yellow.addEventListener("mouseout",function() { changeBackground('yellow') }); /* A LOT OF FUUUUUUUUUUNNNNNNNNNN */
    blue.addEventListener("click",function() { changeBackground('blue') });

})();
